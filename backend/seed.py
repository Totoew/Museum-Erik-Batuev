async def seed():
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async with SessionLocal() as db:
        # ── Администратор ─────────────────────────────────────────────────────
        from sqlalchemy import select
        existing_admin = await db.scalar(
            select(User).where(User.email == "admin@batuev-museum.ru")
        )
        if not existing_admin:
            admin = User(
                name="Администратор музея",
                email="admin@batuev-museum.ru",
                hashed_password=hash_password("Admin2026!"),
                role=UserRole.admin,
                is_active=True,
            )
            db.add(admin)
            print("✓ Создан администратор: admin@batuev-museum.ru / Admin2026!")
        else:
            print("  Администратор уже существует")

        # ── Статьи ────────────────────────────────────────────────────────────
        existing_count = await db.scalar(
            select(Article).limit(1)
        )
        if not existing_count:
            for data in ARTICLES:
                article = Article(**data)
                db.add(article)
            print(f"✓ Добавлено {len(ARTICLES)} статей")
        else:
            print("  Статьи уже существуют")

        # ── Тесты ─────────────────────────────────────────────────────────────
        existing_test = await db.scalar(select(Test).limit(1))
        if not existing_test:
            for tdata in TESTS:
                questions_data = tdata.pop("questions")
                test = Test(**tdata)
                db.add(test)
                await db.flush()
                for i, qdata in enumerate(questions_data):
                    q = Question(test_id=test.id, order=i, **qdata)
                    db.add(q)
            print(f"✓ Добавлено {len(TESTS)} тестов")
        else:
            print("  Тесты уже существуют")

        await db.commit()

    await engine.dispose()
    print("\n✅ Seed завершён!")


if __name__ == "__main__":
    asyncio.run(seed())
